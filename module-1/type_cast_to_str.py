#------- Type Casting to str ---#

#---- int to str -----#
print('---- int to str -----')
a = 10          	# int value
b = str(a)
print(type(b))      # <class 'str'>
print(b)            # 10

#---- float to str -----#
print('---- float to str -----')
a = 10.0444        # float value
print(str(a))      # 10.0444

#---- complex to str -----#
print('---- complex to str -----')
a = 10.0444+1j        # complex value
print(str(a))         # (10.0444+1j)

#---- bool to str -----#
print('---- bool to str -----')
a = True        # bool value
print(str(a))   # True
a = False       # bool value
print(str(a))   # False









