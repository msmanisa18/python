#------- Python Data Types Example --------#

#----- int data type example --------------#
a = 20
print(a)        #==> 20
print(type(a))  #==> <class 'int'>

#----- float data type example ------------#
a = 20.2
print(a)        #==> 20.2
print(type(a))  #==> <class 'float'>

#----- complex data type example ----------#
a = 10 + 20j    #We can use small j
print(a)        #==> (10+20j)
print(type(a))  #==> <class 'complex'>

a = 10 + 30J    #We can use capital j
print(a)        #==> (10+30j)
print(type(a))  #==> <class 'complex'>

#----- bool data type example -------------#
a = True
print(a)        #==> True
print(type(a))  #==> <class 'bool'>

#----- str data type example --------------#
a = 'Test Data'
print(a)        #==> Test Data
print(type(a))  #==> <class 'str'>

a = "Test Data Example"
print(a)        #==> Test Data Example
print(type(a))  #==> <class 'str'>
