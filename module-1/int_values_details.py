#------- Representation of integral values ---------#

#------Decimal Example -No prefix required----------#
#-Because, decimal format is Default number system -#
a = 11
print(a) #==> 11

#----- Binary Example, Prefix -> 0b or 0B ------#
a = 0b1011
print(a) #==> 11 
#-- we will get the output in decimal format, and 1011 in binary is same as 11 in decimal --#

#----- Octal Example, Prefix -> 0o or 0O ------#
a = 0O13
print(a) #==> 11 
#-- we will get the output in decimal format, and 13 in Octal is same as 11 in decimal --#

#----- Hexa Decimal Example, Prefix -> 0x or 0X ------#
a = 0xb
print(a) #==> 11 
#-- Get the output in decimal format. B or b in Hexa Decimal is same as 11 in decimal --#